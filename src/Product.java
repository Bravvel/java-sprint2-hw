public class Product {
    String itemName;
    Boolean isExpence;
    int quantity;
    int sumOfOne;
    int month;

    public Product(String itemName, Boolean isExpence, int quantity, int sumOfOne, int month) {
        this.itemName = itemName;
        this.isExpence = isExpence;
        this.quantity = quantity;
        this.sumOfOne = sumOfOne;
        this.month = month;
    }
}
